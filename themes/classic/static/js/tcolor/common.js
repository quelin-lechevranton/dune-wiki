function hue(l,s,c) { 
    if (s == 0) return 0;
    return Math.round(360 * (1 - c/circleNumber(l,s)));
}
function saturation(l,s) { 
    if (s == 0) return 0;
    return saturationArray[l-1][s-1];
}
function lightness(l,s) { 
    if (s == 0) {
        if (l == 1) return 100;
        if (l == 9) return 0;
    }
    return 100 - 10 * l;
}
function colorName(l,s,c) { 
    if (Number(s) == 0) {
        return greyNames[layersWithGrey.indexOf(Number(l))];
    }
    const rotorSize = circleNumber(l,s) / 6;
    const r = c % rotorSize ? 1 : 0;
    const q = Math.floor(c / rotorSize);
    return colorNames[2*q+r] + nameNumbers[l-1][s-1][c];
}